#!/usr/bin/env node
/**
 * Compute the SHA-256 of public/sleeve/main.wasm and write it as a
 * TypeScript constant in src/keyring/sleeveWasmHash.ts so the bundle has
 * the expected hash baked in at build time.
 *
 * Runs automatically as the `prebuild` script before `npm run build`. Also
 * safe to run by hand after rebuilding the Sleeve WASM.
 *
 * Why: defends against a Cloudflare Worker compromise or service-worker
 * cache poisoning swapping in a modified WASM. At runtime, the Sleeve TS
 * wrapper hashes the fetched bytes and compares to this constant; mismatch
 * fails closed (refuses to generate accounts) instead of silently running
 * the swapped module.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { createHash } from 'node:crypto';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const wasmPath = join(here, '..', 'public', 'sleeve', 'main.wasm');
const outPath = join(here, '..', 'src', 'keyring', 'sleeveWasmHash.ts');

const wasm = readFileSync(wasmPath);
const sha256 = createHash('sha256').update(wasm).digest('hex');

// Note the explicit `: string` and `: number` annotations: without them,
// TypeScript narrows these to literal types like
//   `'0f9edbb4...' & string` / `3392087 & number`
// which makes any comparison against another value (e.g. the placeholder
// sentinel in sleeve.ts) trip TS2367 "no overlap". The annotations widen
// the inferred type back to plain string/number so runtime checks compile.
const content = `// AUTO-GENERATED — DO NOT EDIT BY HAND.
// Regenerated by scripts/hash-sleeve-wasm.mjs as the npm \`prebuild\` step.
// Source: ../../public/sleeve/main.wasm
//
// At runtime, the Sleeve TS wrapper hashes the fetched WASM bytes and
// compares against SLEEVE_WASM_SHA256. Mismatch means the deployed WASM
// has been modified relative to the build's expectation — by a Cloudflare
// compromise, service-worker cache poisoning, or honest mistake — and
// the wallet refuses to instantiate it rather than running the swapped
// module silently.

export const SLEEVE_WASM_SHA256: string = '${sha256}';
export const SLEEVE_WASM_BYTES: number = ${wasm.length};
`;

writeFileSync(outPath, content, 'utf8');
console.log(
  `hash-sleeve-wasm: SHA-256 ${sha256} (${wasm.length} bytes) → src/keyring/sleeveWasmHash.ts`
);
